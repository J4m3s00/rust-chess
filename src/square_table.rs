use crate::base_types::{Position, Color};


pub fn square_table_read(table : &[i32; 64], square : Position, color : Color) -> i32 {
    if square.is_valid() {
        table[match color {
            Color::White => {
                square.index()
            },
            Color::Black => {
                ((7 - square.get_row()) * 8 + (7 - square.get_col())) as usize
            }
        }]
    } else {
        0
    }
}

